            </p>
            <ul class="arrow-marked-list">
              <li>
                <p class="bold">Модульные — проверка каждого компонента или модуля. Для проведения такого рода тестов нужны знания в программировании. Поэтому модульные тесты делают только программисты.</p>
              <li>
                <p class="bold">End-to-end (сквозные) — воздействие на внешние интерфейсы, через встроенные пользовательские функции, после предварительного создания сценариев, приближенных к реальной эксплуатации.</p>
              </li>
              <li>
                <p class="bold">Smoke testing (испытание дымом) позволяют проверить, работают ли критически важные функции AUT (Application Under Test) согласно ожиданиям. Применяются при выпуске новой версии.</p>
              </li>
              <li>
                <p class="bold">Sanity (санитарное) testing — набор испытаний для улучшения работы важных функций. Результат этого теста используется для определения степени стабильности перед проведением дальнейших испытаний.</p>
              </li>
              <li>
                <p class="bold">Интеграционные — проверка совместимости, интеграция подсистем. Выполняются с помощью -Jasmine, Mocha. Опираются на готовые после модульных тестов компоненты. При аутсорсинге тестирования программного обеспечения, наши инженеры дополнительно пользуются собственными библиотеками.</p>
              </li>
              <li>
                <p class="bold">Регрессионные запускают после внесения изменений, чтобы убедиться, что изменение или добавление не нарушило текущую функциональность. Regress testing ловит ошибки, случайно введенные в новую сборку и гарантирует, что устраненные ошибки продолжают оставаться мертвыми.</p>
              </li>
              <li>
                <p class="bold">Тестирование белого и черного ящика. Black box testing используются для исследования функциональности AUT на основе спецификаций SRS без знаний технологии, используемой для реализации программы. White box testing использует ссылку на внутреннюю структуру.</p>
              </li>
              <li>
                <p class="bold">Тесты интерфейса. Изучают соответствие пользовательского интерфейса со спецификацией требований. Проводят с помощью автоматизированных инструментов тестирования пользовательского интерфейса — Monkey test, Saucelabs и Protractor.</p>
